package com.ray.thinkinginjava.basic;

public class SwitchSample {
    public static void main(String[] args) {
        int i = 0;
        switch (i){
            case 1: break;
            case 2: break;
            case 3: break;
            default:
        }

        String s = "";
        switch (s){
            case "AAA": break;
            case "BBB": break;
            default:
        }

        Day c = Day.Monday;
        switch (c){
            case Monday: break;
            case Tuesday: break;
            case Wednesday: break;
            default:
        }
    }
}

